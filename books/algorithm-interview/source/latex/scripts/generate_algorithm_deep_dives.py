def format_workshop(template: str, problem: Problem) -> str:
    values = {
        "model": problem.model.rstrip("。.!！；; "),
        "focus": problem.focus.rstrip("。.!！；; "),
        "edge": problem.edge.rstrip("。.!！；; "),
        "variant": problem.variant.rstrip("。.!！；; "),
    }
    return escape_text(template.format(**values))


