def escape_text(text: str) -> str:
    preserved: dict[str, str] = {}

    def preserve_code(match: re.Match[str]) -> str:
        key = f"@@BOOKCODE{len(preserved)}@@"
        preserved[key] = rf"\code{{{match.group(1)}}}"
        return key

    text = re.sub(r"\\code\{([^{}]*)\}", preserve_code, text)
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
    }
    for source, target in replacements.items():
        text = text.replace(source, target)
    for key, value in preserved.items():
        text = text.replace(key, value)
    return text


def sentence(text: str) -> str:
    return escape_text(text.rstrip("。.!！；; "))


def leetcode_title(problem: Problem) -> str:
    return f"LeetCode {problem.number} {problem.title}"


def label_leetcode_paragraph(paragraph: str) -> str:
    return re.sub(r"^(\d+)\s+", r"LeetCode \1 ", paragraph)


